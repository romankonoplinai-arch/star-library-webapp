// –§–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏–µ –≥—Ä–∞–¥—É—Å–æ–≤
export function formatDegree(degree: number): string {
  const sign = Math.floor(degree / 30)
  const signDegree = degree % 30
  const wholeDegree = Math.floor(signDegree)
  const minutes = Math.round((signDegree - wholeDegree) * 60)
  return `${wholeDegree}¬∞ ${minutes}'`
}

